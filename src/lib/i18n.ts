export function getLocalizedMonthAcc(locale: Locale, index: number) {
  return t(locale, MONTH_ACC_KEYS[index] ?? 'januaryAcc');
}

export function getLocalizedCategoryName(locale: Locale, name: string) {
  const key = CATEGORY_KEY_BY_NAME[name];
  return key ? t(locale, key) : name;
}

export function withDisplayName(locale: Locale, categories: Category[]) {
  return categories.map((category) => ({
    ...category,
    displayName: getLocalizedCategoryName(locale, category.name),
  }));
}
